import UIKit

extension UIColor {
    
    static var lightBeige: UIColor {
        return UIColor(red: 0.957, green: 0.934, blue: 0.906, alpha: 1)
    }
    
    static var darkGrayBlue: UIColor {
        return UIColor(red: 0.52, green: 0.492, blue: 0.555, alpha: 1)
    }
    
    static var paleGrayBlue: UIColor {
        return UIColor(red: 0.4, green: 0.6, blue: 0.80, alpha: 1)
    }
    
    static var silver: UIColor {
        return UIColor(red: 0.753, green: 0.753, blue: 0.753, alpha: 1)
    }
}
