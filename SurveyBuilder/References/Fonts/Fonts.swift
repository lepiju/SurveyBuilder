import UIKit

extension UIFont {
    
    static var avertaRegular: UIFont {
        return UIFont(name: "Averta-Regular", size: UIFont.labelFontSize)!
    }
    
    static var fontAwesomeRegular: UIFont {
        return UIFont(name: "FontAwesome5Free-Regular", size: UIFont.labelFontSize)!
    }
    
    static var fontAwesomeSolid: UIFont {
        return UIFont(name: "FontAwesome5Free-Solid", size: UIFont.labelFontSize)!
    }
    
    static var fontAwesomeBrand: UIFont {
        return UIFont(name: "FontAwesome5Brands-Regular", size: UIFont.labelFontSize)!
    }
}


